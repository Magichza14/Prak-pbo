# Meminta input dari user untuk batas perulangan
n = int(input("Masukkan panjang sisi persegi: "))

# perulangan untuk membuat baris sebanyak n
for i in range(n):

    # perulangan untuk mencetak "*" sebanyak n
    for j in range(n):

        # mencetak "*" sebanyak n
        print("*", end="")

    # membuat baris baru
